class Command(BaseCommand):
    help = 'Seed the food database with ~120 verified food items across all categories.'

    def handle(self, *args, **options):
        created = 0
        skipped = 0
        for data in FOODS:
            name = data['name']
            if FoodItem.objects.filter(name=name).exists():
                skipped += 1
                self.stdout.write(f'  skip  {name}')
            else:
                FoodItem.objects.create(**data)
                created += 1
                self.stdout.write(self.style.SUCCESS(f'  + {name}'))

        self.stdout.write(self.style.SUCCESS(
            f'\nDone: {created} created, {skipped} skipped (already existed).'
        ))
